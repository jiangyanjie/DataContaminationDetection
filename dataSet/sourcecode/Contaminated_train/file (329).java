    private boolean login(String username, String password){
        //Will access DB and load info once DB is configured
        //Currently, tests single name and password

        boolean success = false;
        

        
        //IF username has match
        if(username.equals(TEST_USER)){
            //test if password matches stored password
            if(password.equals(TEST_PASS)){
                //name+pass match, set account details
                account.setEmail(TEST_EMAIL);
                account.setPassword(TEST_PASS);
                account.setUsername(TEST_USER);
                //Set flag to true
                success = true;
            }
        }
        else {  //Username not found
            JOptionPane.showMessageDialog(null, "Invalid username. Please try again");
            success = false;
        }
        
        //Return result of login
        return success;
    }
    
