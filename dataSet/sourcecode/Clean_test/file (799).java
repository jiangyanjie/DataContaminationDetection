    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
         
        if(modCliente == null){
            try{
                taller.crearCliente(nameText.getText(),dniText.getText(),
                            dirText.getText(),tlfText.getText(),
                            mvlText.getText(),mailText.getText(),
                            obsText.getText());
                this.dispose();
            }
            catch(Exception e){
                excepWind w = new excepWind(new javax.swing.JFrame(),true,e.getMessage());
                w.show();
            }
        }
        else{
            try{
                taller.modificarCliente(modCliente.getNombre(),nameText.getText(),dniText.getText(),
                            dirText.getText(),tlfText.getText(),
                            mvlText.getText(),mailText.getText(),
                            obsText.getText());
                this.dispose();
            }
            catch(Exception e){
                excepWind w = new excepWind(new javax.swing.JFrame(),true,e.getMessage());
                w.show();
            }
        }
    }//GEN-LAST:event_acceptButtonActionPerformed

