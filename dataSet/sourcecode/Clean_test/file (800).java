    public crearCocheWnd(java.awt.Frame parent, boolean modal, Coche mod) {
        super(parent, modal);
        initComponents();
        taller = ctrlTaller.getInstance();
        modCoche = mod;
        clientsModel = new DefaultListModel();
        
        if(modCoche != null){
            matText.setText(modCoche.getMatricula());
            marcaText.setText(modCoche.getMarca());
            modeloText.setText(modCoche.getModelo());
            numKmText.setText(modCoche.getNumKm());
            numBastText.setText(modCoche.getNumBast());
            propietarioText.setText(modCoche.getPropietario());
        }
        
        cList = taller.searchClientes("");
        clientsModel.clear();
        for(int i=0; i<cList.size(); ++i){
            clientsModel.addElement(cList.get(i).getNombre());
        }
        clientsList.setModel(clientsModel);
    }

