    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if(modCoche == null){
            try{
                taller.crearCoche(matText.getText(),marcaText.getText(),
                                  modeloText.getText(),numKmText.getText(),
                                  numBastText.getText(),propietarioText.getText());
                this.dispose();
            }
            catch(Exception e){
                excepWind w = new excepWind(new javax.swing.JFrame(),true,e.getMessage());
                w.show();
            }
        }
        else{
            try{
                taller.modificarCoche(matText.getText(),modCoche.getMatricula(),marcaText.getText(),
                            modeloText.getText(),numKmText.getText(),
                            numBastText.getText(),propietarioText.getText());
                this.dispose();
            }
            catch(Exception e){
                excepWind w = new excepWind(new javax.swing.JFrame(),true,e.getMessage());
                w.show();
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

